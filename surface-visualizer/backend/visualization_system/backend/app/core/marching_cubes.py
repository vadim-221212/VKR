    def __init__(self, f_func: Callable, bounds: dict, resolution: int = 50):
        """
        Инициализация алгоритма Marching Cubes

        Args:
            f_func: функция F(x, y, z) для неявной поверхности F(x,y,z)=0
            bounds: границы области {'x_min', 'x_max', 'y_min', 'y_max', 'z_min', 'z_max'}
            resolution: разрешение сетки (количество ячеек по каждому измерению)
        """
        self.f_func = f_func
        self.bounds = bounds
        self.resolution = resolution

        # Создаем 3D сетку
        self.x = np.linspace(bounds['x_min'], bounds['x_max'], resolution + 1)
        self.y = np.linspace(bounds['y_min'], bounds['y_max'], resolution + 1)
        self.z = np.linspace(bounds['z_min'], bounds['z_max'], resolution + 1)
        self.X, self.Y, self.Z = np.meshgrid(self.x, self.y, self.z, indexing='ij')

        # Вычисляем значения функции на сетке
        print(f"Вычисление значений функции на сетке {resolution + 1}x{resolution + 1}x{resolution + 1}...")
        self.values = self.f_func(self.X, self.Y, self.Z)
        print(f"Вычислено {self.values.size} точек")

    def _interpolate_vertex(self, p1: List[float], p2: List[float], val1: float, val2: float, iso_value: float) -> List[float]:
        """Линейная интерполяция между двумя точками"""
        if abs(val1 - val2) < 1e-10:
            t = 0.5
        else:
            t = (iso_value - val1) / (val2 - val1)

        return [
            p1[0] + t * (p2[0] - p1[0]),
            p1[1] + t * (p2[1] - p1[1]),
            p1[2] + t * (p2[2] - p1[2])
        ]

    def extract_surface(self, iso_value: float = 0.0) -> Tuple[List[List[float]], List[int]]:
        """Извлечение изоповерхности из 3D-поля значений."""
        values = np.asarray(self.values, dtype=np.float32)

        if values.ndim != 3:
            raise ValueError("Ожидался трехмерный массив значений для Marching Cubes")

        finite_mask = np.isfinite(values)
        if not np.any(finite_mask):
            return [], []

        finite_values = values[finite_mask]
        fill_value = float(np.max(np.abs(finite_values)) + 1.0)
        values = np.nan_to_num(values, nan=fill_value, posinf=fill_value, neginf=-fill_value)

        min_val = float(np.min(values))
        max_val = float(np.max(values))
        if iso_value < min_val or iso_value > max_val:
            return [], []

        spacing = (
            float(self.x[1] - self.x[0]),
            float(self.y[1] - self.y[0]),
            float(self.z[1] - self.z[0])
        )

        verts, faces, _, _ = skimage_marching_cubes(
            values,
            level=iso_value,
            spacing=spacing,
            allow_degenerate=False
        )

        origin = np.array([self.x[0], self.y[0], self.z[0]], dtype=np.float32)
        verts = verts + origin

        vertices = verts.astype(float).tolist()
        triangles = faces.astype(int).ravel().tolist()

        print(f"Сгенерировано вершин: {len(vertices)}, треугольников: {len(triangles) // 3}")
        return vertices, triangles
